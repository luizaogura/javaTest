public class ObjetosString {

    public static void main(final String[] args) {

        String eu = "Eu";
        String ja = " já";
        String sei = " sei";
        String usar = " usar";
        String strings = " Strings";
        String em = " em";
        String java = " Java";

        String frase = eu + ja + sei + usar + strings + em + java;
        System.out.println(frase); // Eu já sei usar Strings em Java
    }
}